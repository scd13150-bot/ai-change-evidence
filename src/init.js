import {
  existsSync,
  mkdirSync,
  readFileSync,
  writeFileSync,
} from 'fs';
import { dirname, join, resolve } from 'path';

const ACE_GITIGNORE_BLOCK = [
  '# ACE local handoff artifacts',
  '.ace/',
].join('\n');

export function initProject({ workdir = null, force = false, codex = false } = {}) {
  const projectDir = resolve(workdir || process.cwd());
  const aceDir = join(projectDir, '.ace');
  const files = [];

  const aceDirExisted = existsSync(aceDir);
  mkdirSync(aceDir, { recursive: true });
  files.push({
    path: aceDir,
    role: 'state-directory',
    status: aceDirExisted ? 'exists' : 'created',
  });

  files.push(writeManagedFile({
    path: join(projectDir, 'ace.config.json'),
    role: 'project-config',
    content: defaultConfigText(),
    force,
  }));
  files.push(writeManagedFile({
    path: join(projectDir, 'AGENTS.md'),
    role: 'agent-instructions',
    content: agentInstructionsText(),
    force,
  }));
  files.push(ensureGitignoreIgnoresAce(projectDir));

  if (codex) {
    files.push(writeManagedFile({
      path: join(projectDir, '.codex', 'config.toml'),
      role: 'codex-mcp-config',
      content: codexConfigText(projectDir),
      force,
    }));
    files.push(writeManagedFile({
      path: join(projectDir, '.codex', 'hooks.json'),
      role: 'codex-hooks-config',
      content: codexHooksText(),
      force,
    }));
    files.push(writeManagedFile({
      path: join(projectDir, '.codex', 'hooks', 'ace-stop-handoff.cjs'),
      role: 'codex-stop-hook',
      content: codexStopHookText(),
      force,
    }));
  }

  return {
    schema: 'ace.init.v1',
    projectDir,
    files,
    nextSteps: [
      'Run ace start "current objective" before an AI coding task to open a new active task boundary.',
      'Have agents read ace experience --json before editing files.',
      'Run validations through ace run "command" so failures become durable evidence.',
      'Run ace brief --compact 8k before handing the task to another agent.',
      ...(codex ? [
        'Trust the generated .codex project config/hooks in Codex, then start a fresh desktop thread.',
        'Use ace_experience, ace_note, ace_run, and ace_brief through the ACE MCP server when available.',
      ] : []),
    ],
  };
}

export function formatInitSummary(result) {
  const lines = [];
  lines.push(`ACE initialized: ${result.projectDir}`);
  lines.push('Files:');
  for (const file of result.files) {
    lines.push(`  - ${file.status} ${file.path} (${file.role})`);
  }
  lines.push('Next steps:');
  for (const step of result.nextSteps) {
    lines.push(`  - ${step}`);
  }
  return lines.join('\n');
}

function writeManagedFile({ path, role, content, force }) {
  const existed = existsSync(path);
  if (existed && !force) {
    return {
      path,
      role,
      status: 'skipped-existing',
    };
  }
  mkdirSync(dirname(path), { recursive: true });
  writeFileSync(path, content);
  return {
    path,
    role,
    status: existed ? 'overwritten' : 'created',
  };
}

function ensureGitignoreIgnoresAce(projectDir) {
  const path = join(projectDir, '.gitignore');
  const role = 'local-artifact-ignore';
  if (!existsSync(path)) {
    writeFileSync(path, `${ACE_GITIGNORE_BLOCK}\n`);
    return { path, role, status: 'created' };
  }

  const current = readFileSync(path, 'utf-8');
  if (/(^|\r?\n)\.ace\/?(\r?\n|$)/.test(current)) {
    return { path, role, status: 'exists' };
  }

  const separator = current.endsWith('\n') ? '' : '\n';
  writeFileSync(path, `${current}${separator}\n${ACE_GITIGNORE_BLOCK}\n`);
  return { path, role, status: 'updated' };
}

