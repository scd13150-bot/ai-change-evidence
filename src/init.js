function agentInstructionsText() {
  return `# Agent Instructions

## ACE Experience Handoff

This project uses ACE to transfer engineering experience between AI coding sessions, agents, and platforms.

Before changing files:

- Run \`ace experience --json\` and read the objective, recent timeline, failed commands, do-not-repeat notes, and missing evidence.
- If no objective exists, ask the user for the current task or run \`ace start "objective"\` when the task is clear. This starts a new active task boundary without deleting older ledger history.
- Check failed commands before proposing a fix.

During work:

- Run validations through \`ace run "command"\` instead of running important checks directly.
- Record abandoned paths with \`ace note "Do not repeat ..."\`.
- Keep the next step small enough to validate with one command or one focused inspection.

Before handoff:

- Run \`ace brief --compact 8k\` to write \`.ace/handoff.md\`.
- If another agent will continue immediately, include \`ace experience --json\` output or point it to the handoff brief.

Privacy:

- ACE stores command output and notes under \`.ace/\`. Treat this as local task evidence unless the team explicitly decides to commit or share it.
`;
}

function tomlString(value) {
  return JSON.stringify(String(value));
}

function toSlashPath(path) {
  return String(path || '').replace(/\\/g, '/');
}
